char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			if (*(str - 1) < '0' || *(str - 1) > '9' & *(str - 1) < 'A' ||
			*(str - 1) > 'Z' & *(str - 1) < 'a' || *(str - 1) > 'z')
				*str -= 32;
		if (*str >= 'A' && *str <= 'Z')
			if (*(str - 1) >= '0' & *(str - 1) <= '9' || *(str - 1) >= 'A'
			& *(str - 1) <= 'Z' || *(str - 1) >= 'a' & *(str - 1) <= 'z')
				*str += 32;
		str++;
		i++;
	}
	str -= i;
	return (str);
}
