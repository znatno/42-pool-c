int		ft_atoi(char *str)
{
	int v;
	int m;

	v = 0;
	m = 0;
	while (*str <= 32)
		*str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			m = 1;
		*str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		v *= 10;
		v += *str - '0';
		*str++;
	}
	return (m ? -v : v);
}
