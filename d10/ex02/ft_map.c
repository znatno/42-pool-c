int		*ft_map(int *tab, int length, int (*f)(int))
{
	char	*r;
	int		i;

	i = 0;
	r = (char*)malloc(sizeof(char) * length);
	while (i < length)
	{
		r[i] = f(tab[i]);
		i++;
	}
	return (r);
}
