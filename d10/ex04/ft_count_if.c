int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int r;

	i = 0;
	r = 0;
	while (**tab)
	{
		if (f(tab[i]))
		{
			i++;
			r++;
		}
		i++;
	}
	return (r);
}
