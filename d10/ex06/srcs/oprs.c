int		f_plus(int a, int b)
{
	return (a + b);
}

int		f_minus(int a, int b)
{
	return (a - b);
}

int		f_mult(int a, int b)
{
	return (a * b);
}

int		f_div(int a, int b)
{
	return (a / b);
}

int		f_mod(int a, int b)
{
	return (a % b);
}
