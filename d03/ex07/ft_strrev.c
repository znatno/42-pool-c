char	*ft_strrev(char *str)
{
	int		p1;
	int		p2;
	char	b;

	p1 = 0;
	p2 = 0;
	while (*(str + p2) != '\0')
		p2++;
	p2--;
	while (p1 < p2)
	{
		b = str[p1];
		str[p1] = str[p2];
		str[p2] = b;
		p1++;
		p2--;
	}
	return (str);
}
