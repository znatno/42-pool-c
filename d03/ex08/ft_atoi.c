int		ft_atoi(char *str)
{
	int nb;
	int znak;

	nb = 0;
	znak = 1;
	while (*str++ == ' ')
	{
		;
	}
	--str;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			znak = -1;
		str++;
	}
	while ((*str >= '0' && *str <= '9'))
	{
		nb *= 10;
		nb += (int)(*str - '0');
		str++;
	}
	return (nb * znak);
}
