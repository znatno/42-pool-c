int		ft_find_next_prime(int nb)
{
	int i;
	int pf;
	int max_i;

	pf = 0;
	max_i = nb / 2;
	if (nb < 2)
		return (2);
	while (!pf)
	{
		i = 2;
		pf = 1;
		while (i <= max_i)
		{
			if (nb % i == 0)
				pf = 0;
			i++;
		}
		nb++;
	}
	nb--;
	return (nb);
}
