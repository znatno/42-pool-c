int		ft_valid(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == 'A' || str[i] == 'B' || str[i] == 'C' || str[i] == ' ')
			i++;
		else if (str[i] == '-' || str[i] == 'o' || str[i] == '|')
			i++;
		else if (str[i] == '/' || str[i] == '*' || str[i] == '\\')
			i++;
		else
			return (1);
	}
	if (!str)
		return (1);
	else
		return (0);
}
